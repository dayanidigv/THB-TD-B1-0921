// Making Decisions with If

// Check if something is true
let age = 20;

if (age > 18) {
    console.log("You are adult");
}

// Use if and else
let score = 95;

if (score > 90) {
    console.log("Grade A");
}
else {
    console.log("Grade B");
}

// Check many things
let weather = "xyz";

if (weather == "sunny") {
    console.log("Wear sunglasses");
}

else if (weather == "rainy") {
    console.log("Take umbrella");
}

else {
    console.log("Check weather");
}

// Check true or false
let isHappy = true;

if (isHappy) {
    console.log("Smile!");
}
