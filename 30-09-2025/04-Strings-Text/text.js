// Working with Text

// Make text variables
let firstName = "John";
let lastName = "Smith";

console.log("First Name:", firstName);
console.log("Last Name:", lastName);

// Put text together
let fullName = firstName + " " + lastName;

console.log("Full Name:", fullName);

// Mix text with numbers
let message = "I am " + 25 + " years old";
console.log(message);

// Make longer text
let story = "Today is a good day";
console.log("Story:", story);

let multiline = `This is a longer text.
It can go over multiple lines.
This is line 3.`;


console.log("Multiline:", multiline);